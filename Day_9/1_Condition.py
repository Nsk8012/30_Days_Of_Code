# Condition are executable block of statement which is passed only if it passed true
# IF
n = 3
if n > 0:
    print('It\'s a Positive Number')


# Else:
n = -5
if n > 0:
    print("It's a Positive Number")
else:
    print("It's a Negative Number")

# Elif:
n = 0
if n > 0:
    print("It's a Positive Number")
elif n == 0:
    print('It\'s a Zero')
else:
    print("It's a Negative Number")
