# Slicing items from a list
# use index values for slicing a list
fruits = ['banana', 'orange', 'mango', 'lemon']
# Using Positive Index
print(fruits[0:3])      # Return the value according to the index in list
print(fruits[0:])       # It Returns all the values in list
print(fruits[::2])      # It will take every second item in the list
# Using Negative Index
print(fruits[-4:])      # It returns all the values in list
print(fruits[-3:-1])    # Avoiding the last value
print(fruits[::-1])     # Takes negative steps in reverse order of list
