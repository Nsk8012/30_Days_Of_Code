# Tuple with Intial Values
tp = ('item1', 'items2', 'item3', 'items4')
print('Created Tuple is', tp)
# Len() method is used to get length of tuple
print(len(tp))
# Index of list
# (a, b, c, d, e)
#  0, 1, 2, 3, 4    - In Positive Indexing
# -5,-4,-3,-2,-1    - In Negative Indexing
# calling values in list by index
# Positive Index
print(tp[1])
print(tp[3])
# Negative Index
print(tp[-2])
print(tp[-4])
